#include "3-calc.h"
/**
*op_add- function to add two numbers
*@a:operator
*@b:operator
*Return:sum of a ,b
*/
int op_add(int a, int b)
{
return (a + b);
}
/**
*op_sub- function to substract two numbers
*@a:operator
*@b:operator
*Return:difference of a and b
*/
int op_sub(int a, int b)
{
return (a - b);
}
/**
*op_mul- function to product  two numbers
*@a:operator
*@b:operator
*Return:product of a and b
*/
int op_mul(int a, int b)
{
return (a * b);
}
/**
*op_div- function to division two numbers
*@a:operator
*@b:operator
*Return:division of a and b
*/
int op_div(int a, int b)
{
return (a / b);
}
/**
*op_mod- function to get remainder of the division two numbers
*@a:operator
*@b:operator
*Return:remainder of a divided b
*/
int op_mod(int a, int b)
{
return (a % b);
}


